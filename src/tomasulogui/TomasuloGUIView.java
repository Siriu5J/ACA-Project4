    private void updateMemory() {
        Memory4000.setText("");
        Memory5000.setText("");

        int dataSize = simulator.memory.getDataSize() / 4;
        for (int i = 0; i < dataSize; i++) {
            String out = Integer.toString(4000 + 4 * i) + ":  " +
                    Integer.toString(simulator.getMemory().getIntDataAtAddr(4000 + 4 * i)) + "\n";
            Memory4000.append(out);
        }
        Memory4000.setCaretPosition(0);

        for (int i = 0; i < 10; i++) {
            String out = Integer.toString(5000 + 4 * i) + ":  " +
                    Integer.toString(simulator.getMemory().getIntDataAtAddr(5000 + 4 * i)) + "\n";
            Memory5000.append(out);
        }
    }

