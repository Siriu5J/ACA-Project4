    private void AssembleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssembleButtonActionPerformed
        try {
            String inString = AssemblerFileField.getText();
            inString = inString.substring(0, inString.length()-2);
            Assembler assembler = new Assembler(inString);
            assembler.assemble();
            AssemblyResultLabel.setForeground(Color.GREEN);
            AssemblyResultLabel.setText("Assembly successful");
              // go ahead and set simulation file to this name
            FileNameField.setText(inString+".mo");
        } catch (IOException ioe) {
            AssemblyResultLabel.setForeground(Color.RED);
            AssemblyResultLabel.setText("Error opening file");
        } catch (MIPSIOException me) {
            AssemblyResultLabel.setForeground(Color.RED);
            AssemblyResultLabel.setText("Assembly Error");
        }
    }//GEN-LAST:event_AssembleButtonActionPerformed

